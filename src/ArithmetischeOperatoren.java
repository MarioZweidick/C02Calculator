public class ArithmetischeOperatoren
{


    public static void main(String[] args)
    {
        int i1 = 1;
        int i2 = 3;

        //addition
        System.out.println(i1+i2);

        //subtraktion
        System.out.println(i1-i2);

        //multiplikation
        System.out.println(i1*i2);

        //Division
        System.out.println(i2/i1);

        //Modulo
        System.out.println(i1%i2);
    }
}
